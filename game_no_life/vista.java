    private void recorre() {
        int x=0,b=0;
        int suma=0,pixel;
        for (int i = 0; i <matriz.length; i++) {
            for (int j = 0; j < matriz[i].length; j++) {
                pixel=matriz[i][j];
                suma=recorredor(i,j);
                if(pixel==0){
                    if (suma==3) {
                        b=1;
                    }else{
                        b=0;
                    }
                }else{
                    if(suma<2||suma>3){
                        b=0;
                    }else{
                        b=1;
                    }
                }
                remplazo[x][0]=i;
                remplazo[x][1]=j;
                remplazo[x][2]=b;
                x++;
            }
        }
        for (int i = 0; i < remplazo.length; i++) {
            matriz[remplazo[i][0]][remplazo[i][1]]=remplazo[i][2];
        }
        this.suma.setText("suma "+suma);
    }
